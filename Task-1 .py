#Creating a List 
even_num = [2,4,6,8,10] 
print("List", even_num)

#Adding Element to the 'even_num' named List 
even_num.append(14)

#Removing Element from the 'even_num' named List 
even_num.remove(2)

#Modifying an Element in the 'even_num' named List 
even_num[4] = 12

# Printing a List 
print("After updating a List :- ", even_num)



